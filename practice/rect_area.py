# Rect Area
# Create a function called rect_area which takes 2 parameters:
# length and width (of a rectangle) and returns the rectangle's
# area. Then print the result of calling rect_area at least 3
# times with various lengths and widths.

def rect_area(length, width):
    return length * width


print(rect_area(5, 6))
print(rect_area(1, 3))
print(rect_area(2, 10))
